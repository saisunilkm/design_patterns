package com.thoughtfocus.designpatterns.structural.decorator;
/**
The Decorator Design Pattern comes under Structural Design Patterns.

In the Decorator Design Pattern we can attach additional responsibilities to an object dynamically. It allows new
functionality of an existing object without altering its structure. So, there is no change to the original class.

Decorator design pattern is easy to maintain and extend when the amount of choices are more. The best example is, a car
having basic features and we can decorate its features like sports car or luxurious car.

 */