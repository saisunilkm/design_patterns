package com.thoughtfocus.designpatterns.structural.composite;
/**
Composite Design Pattern comes under Structural Design Patterns.

This Composite Design Pattern looks like a tree structure. Composite Object is nothing but which holds the multiple
components. The best example for a Composite Object is, In a computer we can take a Cabinet, Which holds or carries
a Hard Disk , MotherBoard and MotherBoard carries CPU and RAM.

Leaf Object is nothing but which will not carry or hold a single component is called Leaf Object.

The best examples for leaf objects are Hard Disk, Mouse, Keyboard etc.

 */