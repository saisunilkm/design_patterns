package com.thoughtfocus.designpatterns.structural.adapter;
/**
Adaptor Design Pattern will come under Structural Design Patterns.

The Adaptor Design Pattern acts like a bridge in between two interface classes. With the help of Adaptor
Design Pattern we can connect two interface classes.

In Adaptor Design Pattern it will allow  the existing interface to another interface class without modifying
the source code.

For Example we can take a Phone Adaptor which converts 220V to 9V.

 */