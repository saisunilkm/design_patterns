package com.thoughtfocus.designpatterns.structural.proxy;
/**
Proxy Design Pattern comes under Structural Design Patterns.

It will act like a protection wall in between two ends. We can take the best example as, if we search for any website
in browser, the proxy will check whether this website is having public access or not. If the website is having public
access it connects to the server, else if the government has banned the website, the website will not open.

So, Proxy Design Pattern will provide the security in between client and server.

 */